        <h2>How to use it?</h2>
        <div class="row imagegrid">
            <div class="col-sm-6">
                <p>The extended Discord RPC integration requires updated core information files for most cores currently available. You can update your core info files by following the instructions below:</p>
                <ol>
                <li>Start RetroArch.</li>
                <li>Go to <b>Online Updater</b>.</li>
                <li>Select <b>Update Core Info Files</b>.</li>
                </ol> 
                <p><i>You need an active internet connection in order to download latest files.</i></p>
                <p>Afterwards, to enable the <b>Discord integration</b>, following the remaining instructions.</p>
                <ol>
                <li>Go to <b>Settings</b> -> <b>Privacy</b>.</li>
                <li>Change <b>Enable Discord</b> from <b>OFF</b> to <b>ON</b>..</li>
                </ol> 
                <p>Other than that, make sure you are running Discord on the same PC as your RetroArch client, and you have <b>Display currently running game as a status message</b> enabled in your Discord settings.</p>
                <p>Sadly, the Discord RPC API does not support remote connections. You can also join our official Discord channel by clicking <b>Chat with Players!</b></p>
