        <h2>What is it?</h2>
        <p>Discord is a proprietary freeware VoIP application and digital distribution platform—designed initially for the video gaming community—that specializes in text, image, video and audio communication between users in a chat channel.</p>
        <p>Discord runs on Windows, macOS, Android, iOS, Linux, and in web browsers. You can find the latest version in <a href="https://discordapp.com/" style="color:blue">here</a>.</p> 

        <div class="row imagegrid">
